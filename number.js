// check whether the number is positive, negative or zero

const number = prompt("enter a number: ");   // input from the user 

if (number > 0)
{
	console.log("The number is positive");  //check if number is greater than zero

}

else if (number == 0)
{
	console.log ("The number is zero");    // check if number is zero
}

else
{
	console.log("The number is negative number"); // check if number is less than zero
}